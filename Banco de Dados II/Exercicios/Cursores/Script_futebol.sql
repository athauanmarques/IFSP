DROP DATABASE IF EXISTS futebol;
CREATE DATABASE IF NOT EXISTS futebol;
USE futebol;
CREATE TABLE Clube (
	idClube int unsigned primary key not null auto_increment,
	nome CHAR(100) not null,
	caixa float8 not null,
    qtdJogador int default 0)ENGINE=InnoDB;

CREATE TABLE Patrocinador(
	idPatrocinador int unsigned primary key not null auto_increment,
	marca CHAR(50) not null,
    valorMensal float8 not null,
    clubePatrocinado int not null references Clube(idClube)ON DELETE NO ACTION)ENGINE=InnoDB;

  CREATE TABLE Jogador(
	idJogador int unsigned primary key auto_increment,
	nome CHAR(100) not null,
    idade int not null,
    posicao  char(30),
    valor float8 not null,
    salario float8 not null,
    idClube int not null references Clube(idClube)ON DELETE NO ACTION)ENGINE=InnoDB;
    
CREATE TABLE Funcionario(
	idFuncionario int unsigned primary key auto_increment,
	nome CHAR(100) not null,
    cargo char(30),
    salario float8 not null,
    clube int not null references Clube(idClube) ON DELETE NO ACTION)ENGINE=InnoDB;
    create index idx_funcionario on Funcionario(idFuncionario);

CREATE TABLE Transacao(
	idClubeVend int references Clube(idClube) ON DELETE NO ACTION,
    idClubeComp int references Clube(idClube) ON DELETE NO ACTION,
    dataTrans	date not null,
    idJogador int references Jogador(idJogador) ON DELETE NO ACTION,
    PRIMARY KEY(idClubeVend,idClubeComp,dataTrans))ENGINE=InnoDB;

