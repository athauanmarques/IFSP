package aula2_operadores;

public class atribuico {

	public static void main(String[] args) {
		// declarar uma variáveis e atribuir valores.
		/*
		 * Duas variaveis do tipo primitivo inteiro e,
		 * uma variável do tipo string
		 * atribuiremos valores e estes serão exibidos ao término da instruções
		 */
		
		int a = 10;
		System.out.println("a: " + a);
		//uma variaável b recebe o valor de a
		
		int b = a;
		System.out.println("b: " + b);
		
		b = 2;
		System.out.println("novo valor de b: " + b);

	}

}
